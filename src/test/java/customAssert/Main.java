package customAssert;

public class Main {

    private String buttonMain;
    private String buttonShorts;
    private String buttonSubscribe;
    private String buttonLibrary;

    public Main(String buttonMain, String buttonShorts, String buttonSubscribe, String buttonLibrary) {
        this.buttonMain = buttonMain;
        this.buttonShorts = buttonShorts;
        this.buttonSubscribe = buttonSubscribe;
        this.buttonLibrary = buttonLibrary;
    }

    public String getButtonMain() {
        return buttonMain;
    }

    public String getButtonShorts() {
        return buttonShorts;
    }

    public String getButtonSubscribe() {
        return buttonSubscribe;
    }

    public String getButtonLibrary() {
        return buttonLibrary;
    }




}
